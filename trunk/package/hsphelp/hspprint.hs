%index
enumprn
プリンタを列挙する
%group
拡張入出力制御命令
%prm
変数 : 結果の文字列が代入される変数名

%inst
接続されているプリンタ名の一覧を文字列として取得します。
複数のプリンタが認識されている場合は、複数行文字列が代入されます。命令実行後に代入されたプリンタの数が、システム変数statに代入されます。
enumprn命令で取得された一覧の１行目から順番にプリンタID0、プリンタID1…とID番号が対応することになります。

